namespace MineSweeper.Components.Atoms;

/// <summary>
/// Defines the visual variants for the Button component.
/// </summary>
public enum ButtonVariant
{
    /// <summary>
    /// Primary action button with prominent blue styling
    /// </summary>
    Primary,

    /// <summary>
    /// Secondary action button with neutral gray styling
    /// </summary>
    Secondary,

    /// <summary>
    /// Success action button with green styling
    /// </summary>
    Success,

    /// <summary>
    /// Danger/destructive action button with red styling
    /// </summary>
    Danger,

    /// <summary>
    /// Warning action button with yellow/orange styling
    /// </summary>
    Warning,

    /// <summary>
    /// Icon-only button with minimal styling
    /// </summary>
    Icon,

    /// <summary>
    /// Outline button with transparent background
    /// </summary>
    Outline,

    /// <summary>
    /// Ghost button with minimal visual presence
    /// </summary>
    Ghost
}
